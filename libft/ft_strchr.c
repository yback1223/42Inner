#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;
	int	size;

	size = 0;
	i = 0;
	while (s[i])
	{
		if (s[i] == (char)c)
			return ((char *)(s + i));
		i++;
	}
	while (s[size])
		size++;
	if (c == 0)
		return ((char *)&s[size]);
	return (0);
}
